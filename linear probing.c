#include <stdio.h>
#define SIZE 10
int hashtable[SIZE];
void init()
{
    for(int i=0;i<SIZE;i++)
    hashtable[i]=-1;
}
void insert(int key)
{
    int index=key%SIZE;
    while(hashtable[index]!=-1)
    {
        index=(index+1)%SIZE;
    }
    hashtable[index]=key;
}
void display()
{
    printf("hashtable:");
    for(int i=0;i<SIZE;i++)
    {
        printf("%d->%d\n",i,hashtable[i]);
    }
}
int main()
{
    int keys[]={23,43,13,27,39};
    int n=5;
    init();
    for(int i=0;i<n;i++)
    {
        insert(keys[i]);
    }
    display();
    return 0;
}

